    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        int pos = jl_clasificacion.getSelectedIndex(), cont = 0;
        if (bt_borradores.isSelected()) {
            for (Persona t1 : ListPersonas) {
                if (t1.getCorreo().equalsIgnoreCase(CorreoActivo)) {
                    for (Mensajes t2 : t1.getListMensajes()) {
                        if (cont == pos) {
                            t1.setListMensajesBorradores(t2);
                        }
                        cont++;
                    }
                }

            }
        }
        if (bt_eliminar.isSelected()) {
            for (Persona t1 : ListPersonas) {
                if (t1.getCorreo().equalsIgnoreCase(CorreoActivo)) {
                    for (Mensajes t2 : t1.getListMensajes()) {
                        if (cont == pos) {
                            t1.getListMensajes().remove(pos);
                            t1.setListMensajes(t2);
                        }
                        cont++;
                    }
                }

            }
        }
        if (bt_importe.isSelected()) {
            for (Persona t1 : ListPersonas) {
                if (t1.getCorreo().equalsIgnoreCase(CorreoActivo)) {
                    for (Mensajes t2 : t1.getListMensajes()) {
                        if (cont == pos) {
                            t1.setListMensajesImportantes(t2);
                        }
                        cont++;
                    }
                }

            }
        }
        if (bt_noDeseado.isSelected()) {
            for (Persona t1 : ListPersonas) {
                if (t1.getCorreo().equalsIgnoreCase(CorreoActivo)) {
                    for (Mensajes t2 : t1.getListMensajes()) {
                        if (cont == pos) {
                            t1.setListMensajesNoDeseados(t2);
                        }
                        cont++;
                    }
                }

            }
        }
        if (bt_spam.isSelected()) {
            for (Persona t1 : ListPersonas) {
                if (t1.getCorreo().equalsIgnoreCase(CorreoActivo)) {
                    for (Mensajes t2 : t1.getListMensajes()) {
                        if (cont == pos) {
                            t1.setListMensajesSpam(t2);
                        }
                        cont++;
                    }
                }

            }
        }
        Buzon.pack();
        Buzon.setVisible(true);
        Buzon.setLocationRelativeTo(this);
        ClasificacionMensajes.setVisible(false);

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton16ActionPerformed

