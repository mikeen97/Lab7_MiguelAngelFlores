    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String nombre = tf_registroNombre.getText(), apellido = tf_registroApellido.getText(), correo = tf_registroCorreo.getText();
        String pais = tf_registroPais.getText(), numeroTelefonico = tf_registroNumeroTelefonico.getText(), contrasena = tf_registroContrasena.getText();
        String confirmarContrasena = tf_registroConfirmarContrasena.getText();
        Date nacimiento = dcNacimiento.getDate();
        boolean correoIgual = true;
        for (Persona t1 : ListPersonas) {
            if (t1.getCorreo().equalsIgnoreCase(correo)) {
                correoIgual = false;
            }
        }
        if (contrasena.equalsIgnoreCase(confirmarContrasena) && correoIgual == true) {
            ListPersonas.add(new Persona(nombre, apellido, correo, nacimiento, pais, nombre, contrasena));
            JOptionPane.showMessageDialog(null, "Usuario creado exitosamente!");

            tf_registroApellido.setText("");
            tf_registroConfirmarContrasena.setText("");
            tf_registroContrasena.setText("");
            tf_registroCorreo.setText("");
            tf_registroNombre.setText("");
            tf_registroNumeroTelefonico.setText("");
            tf_registroPais.setText("");
        } else {
            if (correoIgual == false) {
                tf_registroCorreo.setText("");
                JOptionPane.showMessageDialog(null, "El correo no disponible");
            } else {
                tf_registroConfirmarContrasena.setText("");
                tf_registroContrasena.setText("");
                JOptionPane.showMessageDialog(null, "Las contraseñas son incorrectas");
            }
        }


    }//GEN-LAST:event_jButton5ActionPerformed

